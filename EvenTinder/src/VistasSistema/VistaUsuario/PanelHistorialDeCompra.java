    private void actualizarListaDeCompra() {
        this.compras= this.papa.getControladorUsuario().obtenerInformacionDelHistorialDeCompraDeUnUsuario();
        if(compras==null){
            return;
        }
        modeloLista.removeAllElements();
        for (int i = 0; i < this.compras.size(); i++) {
            this.modeloLista.addElement("Id compra: " + this.compras.get(i).getId() + "   evento: " + this.compras.get(i).getNombreEvento());
        }
        this.listaDeCompras.setModel(modeloLista);
        this.repaint();
        this.revalidate();
    }
    
    public String convertirFechaString(Date date){

       Format formatter = new SimpleDateFormat("dd-MM-yyyy");
       return formatter.format(date);
    }
